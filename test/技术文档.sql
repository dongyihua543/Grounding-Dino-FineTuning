2. Jaccard Index(杰卡德指数)
Jaccard Index(杰卡德指数)通常也称为"杰卡德相似系数"。
这个指数是用于衡量两个集合之间相似性的方法，它计算两个集合的交集元素数目与它们的并集元素数目之比。

3. IoU(交并比)
Intersection-over-Union(IoU)，也称为 Jaccard Index，是一种用于测量目标检测和图像分割等计算机视觉任务性能的常用指标。
IoU 用于评估模型的检测或分割结果与真实标签之间的重叠程度。它通常用于计算两个集合(通常是矩形、边界框或区域)之间的重叠程度。
在目标检测任务中，IoU 常常用于评估检测框(bounding box)与真实目标之间的匹配程度。通常，IoU 大于一个阈值(如0.5)的检测框被认为是正确检测到的目标。
在图像分割任务中，IoU 也用于衡量模型生成的分割区域与真实分割区域之间的相似性。
